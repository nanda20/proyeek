public class DataDosen {
    private String nip_karyawan, jam, nama;
    public DataDosen() {
    }

    public String getNip_karyawan() {
        return nip_karyawan;
    }

    public void setNip_karyawan(String nip_karyawan) {
        this.nip_karyawan = nip_karyawan;
    }

    public String getJam() {
        return jam;
    }

    public void setJam(String jam) {
        this.jam = jam;
    }

    public String getNama() {
        return nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

}
